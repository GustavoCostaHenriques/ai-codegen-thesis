@Injectable({ providedIn: 'root' })
export class I18nService {
  private readonly storageKey = 'weekly-planning.language';
  private readonly languageState = signal<AppLanguage>(this.hydrate());

  readonly language = this.languageState.asReadonly();

  setLanguage(language: AppLanguage): void {
    this.languageState.set(language);
    localStorage.setItem(this.storageKey, language);
  }

  translate(key: string): string {
    return dictionary[this.languageState()][key] ?? dictionary.en[key] ?? key;
  }

  private hydrate(): AppLanguage {
    const stored = localStorage.getItem(this.storageKey);
    return stored === 'pt' ? 'pt' : 'en';
  }
}
